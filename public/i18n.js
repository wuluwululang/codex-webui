let activeLocale = DEFAULT_LOCALE;

export function initI18n() {
  activeLocale = resolveLocale();
  document.documentElement.lang = activeLocale;
  translateDocument();
}

export function t(key, params = {}) {
  const template = dictionaries[activeLocale]?.[key]
    ?? dictionaries[DEFAULT_LOCALE]?.[key]
    ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? ""));
}

export function currentLocale() {
  return activeLocale;
}

export function translateDocument(root = document) {
  for (const element of root.querySelectorAll("[data-i18n]")) {
    element.textContent = t(element.dataset.i18n);
  }
  for (const element of root.querySelectorAll("[data-i18n-placeholder]")) {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  }
  for (const element of root.querySelectorAll("[data-i18n-title]")) {
    element.setAttribute("title", t(element.dataset.i18nTitle));
  }
  for (const element of root.querySelectorAll("[data-i18n-aria-label]")) {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel));
  }
}

function resolveLocale() {
  const languages = navigator.languages?.length ? navigator.languages : [navigator.language || DEFAULT_LOCALE];
  const primaryLanguage = String(languages[0] || DEFAULT_LOCALE).toLowerCase();
  return primaryLanguage.startsWith("zh") ? "zh-CN" : "en-US";
}
